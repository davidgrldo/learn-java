public class OperasiMatematika {
  public static void main(String[] args) {
    int x = 100;
    int y = 10;
    
    System.out.println(x + y);
    System.out.println(x - y);
    System.out.println(x * y);
    System.out.println(x / y);
    System.out.println(x % y);

    int z = 100;

    z += 10;
    System.out.println(z);
    z -= 10;
    System.out.println(z);
    z *= 10;
    System.out.println(z);
    z /= 10;
    System.out.println(z);
    z %= 10;
    System.out.println(z);


    int a = 100;
    a++;
    System.out.println(a);
    a--;
    System.out.println(a);

    boolean b = !true;
    System.out.println(b);
  }
}
